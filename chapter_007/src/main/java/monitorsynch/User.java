package monitorsynch;

/**
 * Класс User для класс хранилища пользователей UserStorage [#1104].
 * Created by Алексей on 24.11.2017.
 */
public class User {
    private int id;
    private int amount;

    public User(int id, int amount) {
        this.id = id;
        this.amount = amount;
    }

    public int getId() {
        return id;
    }

    public int getAmount() {
        return amount;
    }

    public int addAmount(int amount) {
        this.amount += amount;
        return this.amount;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        User user = (User) o;
        return getId() == user.getId();
    }

    @Override
    public int hashCode() {
        return getId();
    }
}
